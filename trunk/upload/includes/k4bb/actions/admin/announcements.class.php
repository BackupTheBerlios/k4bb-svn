class AdminManageAnnouncements extends FAAction {
	function execute(&$request) {		
		
		if($request['user']->isMember() && ($request['user']->get('perms') >= SUPERADMIN)) {
			
			exit('In the 7.1 release.');

		} else {
			no_perms_error($request);
		}

		return TRUE;
	}
